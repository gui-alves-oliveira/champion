/**
 * Retorna todos os torneios disponíveis
 */
export function getTournaments() {
  return tournaments;
}

/**
 * Busca um torneio pelo slug
 * @param {string} slug
 */
export function getTournamentBySlug(slug: string) {
  return tournaments.find((t) => t.slug === slug);
}

/**
 * Retorna os grupos de um torneio específico pelo slug
 * @param {string} slug
 */
export function getGroups(slug: string) {
  const tournament = getTournamentBySlug(slug);
  return tournament?.tournament?.groups ?? [];
}

/**
 * Retorna os brackets de um torneio específico pelo slug
 * @param {string} slug
 */
export function getBrackets(slug: string) {
  const tournament = getTournamentBySlug(slug);
  return tournament?.tournament?.brackets ?? [];
}

/**
 * Retorna os jogos (games) de um torneio específico pelo slug
 * @param {string} slug
 */
export function getGames(slug: string) {
  const tournament = getTournamentBySlug(slug);
  return tournament?.tournament?.games ?? [];
}
